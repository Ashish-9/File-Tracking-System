<h1 align="left"><u>Project Analysis</u>:</h1>
<p>&nbsp;</p>
<p>This  application has been divided in to four modules</p>
<h1>&nbsp;</h1>
<h1>1. <u>Admin Module</u>:</h1>
<p>&nbsp;</p>
<ul type="disc">
  <li>In       this module Administrator will add users to work with files.</li>
  <li>He can       check exact status of the files and he receives completed files from other       departments.</li>
  <li>Â Administrator can delete the files from       database after processing of files. Administrator can see the pending       files list.</li>
</ul>
<p>&nbsp;</p>
<p><strong>2. <u>Department Module</u>:</strong></p>
<p>&nbsp;</p>
<ul>
  <li>The users are </li>
  <ul>
    <ul>
      <li>Clerk</li>
      <li>Superintendent</li>
      <li>Deputy secretary</li>
      <li>Additional Secretary</li>
      <li>Secretary</li>
      <li>Ministry</li>
    </ul>
  </ul>
</ul>
<p>&nbsp;</p>
<ul>
  <li>All the above users receive the files from  other departments to be processed.</li>
  <li>He can accept or reject the files from other departments.</li>
  <li>After completion of process they dispatch the  file to any other department. </li>
</ul>
<p><strong>3. <u>Client  Module</u>:</strong></p>
<p>&nbsp;</p>
<ul type="disc">
  <li>Client       can check the exact status of the file in which department it is       processing.</li>
  <li>If the       file is rejected he can see the reason for the rejection. </li>
</ul>
<p><strong>4. <u>Reports Module:</u></strong> </p>
<p><br />
  In this module administrator will get  different types of reports like Number of employees of a company and files  uploaded etc. This module is controlled by administrator only.</p>
