       	  <h2> About File Tracking System </h2>
        	<p>&nbsp;</p>
<p><strong><u>Existing System</u></strong><strong>:</strong></p>
<p>&nbsp;</p>
<p>In present situation in  any organization file work is mandatory, so every time moving files from one  department other department or from one branch to other branch manually is some  what difficult. To avoid this problem and to to communicate each and every  branch, each and every department we are going to introduce online &ldquo;File  tracking system&rdquo;.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong><u>Proposed System</u></strong></p>
<p>&nbsp;</p>
<p>The main aim of the  project <strong>File Tracking System</strong> is to  provide flexibility to government departments to maintain huge amount of  pending files. This system will help all government departments to keep track  of every file. This web  application is designed for Automation of Department of Industries. </p>
<ul type="disc">
  <li>In this System, We need to process files in Hierarchy       and proper action should be taken and necessary permissions are given for       the requested Files. </li>
  <li>Sometimes we have to take concern of different       departments into consideration. </li>
</ul>
<p>For Example: - Pollution Board  Permissions for some Industries.</p>
