        	<h2>File Tracking System</h2>
        	<p>&nbsp;</p>
        	<p><strong>Purpose:</strong></p>
       	  <p>&nbsp;</p>
<p>File  Tracking System addresses all areas of records management. It allows us to take  an unbiased approach to the document storage and retrieval methodology employed  for any application .It improves the file processing time. It avoids  misplacement of a file. Communicates the user about current status of his/her  file. </p>
<p>&nbsp;</p>
<p><strong>Scope</strong></p>
<p><br />
  • File Tracking System addresses all areas of records management. <br />
  • It allows us to take an unbiased approach to the document storage and  retrieval methodology employed for any application.<br />
  • It improves the file processing time. <br />
  • It avoids misplacement of a file. <br />
  • Communicates the user about current status of his/her file.<br />
  • It will help user to know about the status and make a complaint if it is not  accessed with in time.<br />
</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong> File Tracking System Overview</strong></p>
<p><br />
   <em><strong>Registration of the user with the File Tracking System</strong></em></p>
<p><br />
  A screen with many fields will be provided to the user wherein he can get  registered with the file tracking system. The screen is designed using the  dream weaver cs4 tool. Fields like username, password, mobile number, email id,  address are mandatory in this screen. This information will be very helpful to  the system for further use. The information entered by the user will be stored  in user table in database using the JDBC connectivity.</p>
<p><br />
  <em><strong>User should give the particulars of the File</strong></em><strong></strong></p>
<p><br />
  A screen is provided to the user where he/she needs to give the particulars of  the file like file has to be uploaded. A browse option is provided so that the  user an easily upload the file from the memory of the system. The user will be  provided with a unique id by the file tracking system through which the user  can checks his status. The id will be alive until the file is being processed  once the file gets accessed the id will be expired. The screen is designed  using the dream weaver cs4 tool. The file name will be stored in the file info  table in database using the JDBC connectivity.<br />
  </p>
<p>&nbsp;</p>
<p><em><strong>Users can check the status of their files<br />
  </strong></em></p>
<p>&nbsp;</p>
<p>The user can check the status of his/her file using the id that has been  provided to him by the file tracking system. A screen is provided to him  wherein </p>
<p>he can enter the file id given to him to get the status of his file.  The screen is designed using the dream weaver cs4 tool.<br />
  </p>
<p>&nbsp;</p>
<p><em><strong>User can give a complaint<br />
  </strong></em></p>
<p>&nbsp;</p>
<p>This is the additional feature added to our project where the user can make a  complaint if his/her file is not accessed by the officers of the organization.  </p>
<p>He/she is provided with a screen where along with his id he can type the  message and send it to the head of the organization</p>
